function getValueByPath(object, path) {
    return path.split(".").reduce((accumulator, current) => {
        if (accumulator === undefined || accumulator === null) return undefined;
        return accumulator[current];
    }, object);
}

function applyTranslations(lang = "lv") {
    const currentLanguage = translations[lang] ? lang : "lv";

    document.documentElement.lang = currentLanguage;

    const title = getValueByPath(translations[currentLanguage], "meta.title");
    const description = getValueByPath(translations[currentLanguage], "meta.description");

    if (title) {
        document.title = title;
    }

    const metaDescription = document.querySelector('meta[name="description"]');
    if (metaDescription && description) {
        metaDescription.setAttribute("content", description);
    }

    document.querySelectorAll("[data-i18n]").forEach(element => {
        const value = getValueByPath(translations[currentLanguage], element.dataset.i18n);
        if (typeof value === "string") {
            element.textContent = value;
        }
    });

    document.querySelectorAll("[data-i18n-html]").forEach(element => {
        const value = getValueByPath(translations[currentLanguage], element.dataset.i18nHtml);
        if (typeof value === "string") {
            element.innerHTML = value;
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
        const value = getValueByPath(translations[currentLanguage], element.dataset.i18nPlaceholder);
        if (typeof value === "string") {
            element.setAttribute("placeholder", value);
        }
    });

    document.querySelectorAll("[data-i18n-aria-label]").forEach(element => {
        const value = getValueByPath(translations[currentLanguage], element.dataset.i18nAriaLabel);
        if (typeof value === "string") {
            element.setAttribute("aria-label", value);
        }
    });

    document.querySelectorAll("[data-i18n-alt]").forEach(element => {
        const value = getValueByPath(translations[currentLanguage], element.dataset.i18nAlt);
        if (typeof value === "string") {
            element.setAttribute("alt", value);
        }
    });

    document.querySelectorAll(".lang-btn").forEach(button => {
        button.classList.toggle("active", button.dataset.lang === currentLanguage);
    });

    localStorage.setItem("siteLanguage", currentLanguage);
}

window.applyTranslations = applyTranslations;
window.translations = translations;